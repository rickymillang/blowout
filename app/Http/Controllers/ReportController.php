     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|string',
            'message' => 'required'
        ]);

        $administrator = Report::create([
            'title' => $request->title,
            'message' => $request->message,
            'user_id' => auth()->user()->id
        ]);

        session()->flash('message', 'Report successfully sent');

        return redirect()->back();
    }

