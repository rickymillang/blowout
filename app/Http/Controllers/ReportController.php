        if (auth()->user()->hasRole('superadmin')) {
            $reports = Report::all();
        } else if (auth()->user()->hasRole('establishment.admin') || auth()->user()->hasRole('customer')) {
            $reports = Report::where('user_id', auth()->user()->id)->get();
        }

        return view('reports.index', compact('reports'));
